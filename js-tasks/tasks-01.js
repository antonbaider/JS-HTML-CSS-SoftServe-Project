function evenOrOdd(number) {
  // Треба перевірити число на парність
  if (number % 2 === 0) {
    // Якщо парне - повернути "Even"
    return "Even";
  } else {
    // Якщо непарне - повернути "Odd"
    return "Odd";
  }
}

